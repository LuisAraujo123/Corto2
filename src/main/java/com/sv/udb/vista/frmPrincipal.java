    private void btnGuarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuarActionPerformed
        try {
            if (!this.txtNomb_sere.getText().isEmpty() && !this.txtDesc_sere.getText().isEmpty())
            {
                SeresVivos obje = new SeresVivos();
                //obje.setCodiEqui(Integer.parseInt(txtCodi.getText()));
                obje.setNomb_sere(this.txtNomb_sere.getText());
                obje.setDesc_sere(this.txtDesc_sere.getText());
                SeresVivos refe = (SeresVivos)this.cmbSersVivs.getSelectedItem();
                obje.setCodi_refe_sere(refe.getCodi_sere());
                if (this.cmbSersVivs.getSelectedIndex() == 0)
                {
                    if (new seresVivosCtrl().guar2(obje))
                    {
                        JOptionPane.showMessageDialog(this, "Datos guardados");
                        this.actTab();
                    }
                    else
                    {
                        JOptionPane.showMessageDialog(this, "Error");
                    }
                }
                else
                {
                    if (new seresVivosCtrl().guar(obje))
                    {
                        JOptionPane.showMessageDialog(this, "Datos guardados");
                        this.actTab();
                    }
                    else
                    {
                        JOptionPane.showMessageDialog(this, "Error");
                    }
                }
                
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Por favor complete los campos requeridos (Nombre y descripción) el 'pertenece a' si desea que sea nulo seleccione el primero ");
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: Hubo un problema al guardar los datos");
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnGuarActionPerformed

