    private void limp()
    {
        this.txtcodi_sere.setText("");
        this.txtDesc_sere.setText("");
        this.txtNomb_sere.setText("");
        this.llenCmb();
    }
    
    
    private void actTab() {
        try {
            seresVivosCtrl obje = new seresVivosCtrl();
            DefaultTableModel model = (DefaultTableModel)this.tblSersVivs.getModel();
            while(model.getRowCount()>0){model.removeRow(0);} //Limpiar modelo
            //Se agregan todos los que esten en la lista
            for (SeresVivos temp : obje.consTodo())
            {
                model.addRow(new Object[]{temp, temp.getDesc_sere(), temp.getPadre_sere()});
            }
            limp();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }
    
    private void llenCmb() {
        try {
            seresVivosCtrl obje = new seresVivosCtrl();
            this.cmbSersVivs.removeAllItems();
            DefaultComboBoxModel<SeresVivos> dcm = new DefaultComboBoxModel<>();
            SeresVivos nulo = new SeresVivos();
            nulo.setNomb_sere("-Seleccione uno si desea-");
            dcm.addElement(nulo);
            for (SeresVivos temp : obje.consTodo())
                {
                    dcm.addElement(temp);
                }
            this.cmbSersVivs.setModel((ComboBoxModel)dcm);
        } catch (Exception e) {
            e.printStackTrace();
        }   
    }

