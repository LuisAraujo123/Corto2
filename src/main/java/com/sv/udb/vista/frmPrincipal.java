    private void btnElimActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnElimActionPerformed
        if (this.tblSersVivs.getSelectedRow() > -1)
        {
            try {
                if (!this.txtcodi_sere.getText().isEmpty()) {
                    SeresVivos obje = new SeresVivos();
                    obje.setCodi_sere(Integer.parseInt(this.txtcodi_sere.getText()));
                    if (JOptionPane.showConfirmDialog(this, "Esta seguro de que desea eliminar este registro?", "AVISO", JOptionPane.YES_NO_OPTION) == 0) 
                    {
                        if (new seresVivosCtrl().dele(obje))
                        {
                            JOptionPane.showMessageDialog(this, "Datos Eliminados");
                            this.actTab();
                        }
                        else
                        {
                            JOptionPane.showMessageDialog(this, "Error, no se pudo eliminar el registro");
                        }
                    }
                }
                else
                {
                    JOptionPane.showMessageDialog(this, "Por favor Seleccione un registro (el campo codigo debe estar lleno)");
                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage());
            }
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Por favor seleccione un registro a Eliminar");
        }
    }//GEN-LAST:event_btnElimActionPerformed

    private void btnModiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModiActionPerformed
        try {
            if (!this.txtNomb_sere.getText().isEmpty() && !this.txtDesc_sere.getText().isEmpty() && !this.txtcodi_sere.getText().isEmpty())
            {
                SeresVivos obje = new SeresVivos();
                obje.setCodi_sere(Integer.parseInt(this.txtcodi_sere.getText()));
                obje.setNomb_sere(this.txtNomb_sere.getText());
                obje.setDesc_sere(this.txtDesc_sere.getText());
                SeresVivos refe = (SeresVivos)this.cmbSersVivs.getSelectedItem();
                obje.setCodi_refe_sere(refe.getCodi_sere());
                if (this.cmbSersVivs.getSelectedIndex() == 0)
                {
                    if (new seresVivosCtrl().upda2(obje))
                    {
                        JOptionPane.showMessageDialog(this, "Datos guardados");
                        this.actTab();
                    }
                    else
                    {
                        JOptionPane.showMessageDialog(this, "Error");
                    }
                }
                else
                {
                    if (new seresVivosCtrl().upda(obje))
                    {
                        JOptionPane.showMessageDialog(this, "Datos guardados");
                        this.actTab();
                    }
                    else
                    {
                        JOptionPane.showMessageDialog(this, "Error");
                    }
                }
                
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Por favor complete los campos requeridos (Nombre y descripción) y seleccione un registro");
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: Hubo un problema al guardar los datos");
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnModiActionPerformed

